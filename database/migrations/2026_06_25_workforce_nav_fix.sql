-- الخطأ: سُجِّلت موديولات الطبقة بـ is_link='0' وأيقوناتٍ بلا بادئة نمطٍ ('fa-x')،
--        فتخطّاها مولّد القائمة الديناميكي (includes/dynamic_nav.php يعرض is_link='1'
--        ويطبع الأيقونة حرفياً). الصحيح: السلوك يقوده جدول modules لا تضمينٌ صريحٌ في
--        insidebar.php. هذا التهجير يجعلها تظهر تلقائياً عبر renderDynamicNavigation.
--
-- idempotent: آمنٌ عند إعادة التشغيل (حُرّاسٌ يمنعون التكرار/التضعيف).
-- يقتصر على code LIKE 'Workforce/%' — لا يمسّ موديولات أخرى.
-- ════════════════════════════════════════════════════════════════════════════
SET NAMES utf8mb4;

-- 1) تفعيل الظهور في القائمة الديناميكية
UPDATE `modules`
   SET `is_link` = '1'
 WHERE `code` LIKE 'Workforce/%'
   AND `is_link` <> '1';

-- 2) إضافة بادئة نمط Font Awesome ('fa ') للأيقونات التي بلا بادئة ('fa-x' → 'fa fa-x')
--    الحارس (LIKE 'fa-%') لا يطابق 'fa fa-%' (المحرف الثالث مسافةٌ لا شرطة) ⇒ بلا تضعيف.
UPDATE `modules`
   SET `icon` = CONCAT('fa ', `icon`)
 WHERE `code` LIKE 'Workforce/%'
   AND `icon` LIKE 'fa-%';

